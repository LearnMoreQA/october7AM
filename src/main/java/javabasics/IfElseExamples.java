package javabasics;

public class IfElseExamples {

    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        if(a == b) // if (condition -> true)
        {
            System.out.println("Both Values are Equal");
        }
        else
        {
            System.out.println("Both values are Not Matched");
        }
    }
}
